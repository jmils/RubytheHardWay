#Prints the string indented 5 spaces aka tabbed.
tabby_cat = "\tI'm tabbed in."

#Prints the sting on two lines.
persian_cat = "I'm split\non a line."
#String that escapes the backslash and prints one slash in each part of the string.
backslash_cat = "I'm \\ a \\ cat."

#Prints the each line of the string tabbed in to look like a list. The last line still prints Catnip and Grass on separate lines.
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""


puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat
